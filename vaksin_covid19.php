<h1>Politeknik LP3I Jakarta Kampus Depok Turut Mengadakan kegiatan Vaksinasi COVID-19</h1>

<p>
Pada hari kamis lalu (09/09/21) Politeknik LP3I Jakarta Kampus Depok mengadakan pelaksanaan program vaksinasi Covid-19 dan diikuti oleh warga sekitar.
</p>

<img src="covid-19.2.jpeg">

<p>
Pelaksanaan vaksinasi ini ditinjau langsung oleh Wakil Wali Kota Depok, Imam Budi Hartono didampingi Kepala Kampus LP3I Depok, Farizal Marzuki.
</p>

<img src="covid-19.3.jpeg">

<p>
Program vaksinasi yang diselenggarakan oleh Politeknik LP3I Jakarta ini mendapatkan apresiasi dari Wakil Wali Kota Depok.
</p>

<p>
Membantu meningkatkan cakupan vaksinasi di Kota Depok yang saat ini capaian vaksinasi sebesar 46 persen.
</p>

<img src="covid-19.4.jpeg">

<p>
Percepatan vaksinasi ini juga didasari oleh rencana penggelaran Pembelajaran Tatap Muka (PTM).
</p>

<p>
Di tempat yang sama, Kepala Kampus LP3I Depok, Farizal Marzuki menyebut target 150 mahasiswa vaksin dosis pertama jenis Moderna.
</p>

<img src="covid-19.5.jpeg">

<p>
“Sementara untuk persiapan PTM sudah 100 persen, karena LP3I adalah kampus vokasi agar tercapai tujuan pembelajarannya,” ucap Farizal Marzuki.
</p>

